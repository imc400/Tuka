// Helper para obtener una región por código
export function getRegionByCode(code: string): Region | undefined {
  return CHILE_REGIONS.find((r) => r.code === code);
}

// Helper para obtener todas las comunas de una región
export function getCommunesByRegion(regionCode: string): Commune[] {
  const region = getRegionByCode(regionCode);
  return region?.communes || [];
}

// Helper para buscar una comuna por código
export function getCommuneByCode(regionCode: string, communeCode: string): Commune | undefined {
  const communes = getCommunesByRegion(regionCode);
  return communes.find((c) => c.code === communeCode);
}
